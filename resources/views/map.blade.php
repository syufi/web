               var marker = new google.maps.Marker({
                    position: new google.maps.LatLng(-7.917357, 112.634396),
                    map: map,
                    title: 'Klik Saya'
               });
 
               // Membuat InfoWindow dengan memunculkan informasi/teks ketika di-klik
